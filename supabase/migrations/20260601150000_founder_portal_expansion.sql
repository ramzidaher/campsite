create or replace function public.platform_upsert_catalog_draft_entry(
  p_key text,
  p_label text,
  p_description text,
  p_category text,
  p_is_founder_only boolean,
  p_is_archived boolean
)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  v_draft bigint;
  v_key text := lower(trim(coalesce(p_key, '')));
begin
  if not public.platform_is_founder(auth.uid()) then
    raise exception 'not allowed' using errcode = '42501';
  end if;
  if v_key = '' then
    raise exception 'permission key required';
  end if;
  if trim(coalesce(p_label, '')) = '' then
    raise exception 'permission label required';
  end if;
  select public.platform_ensure_latest_draft_catalog() into v_draft;

  insert into public.platform_permission_catalog_entries (
    version_no, key, label, description, category, is_founder_only, is_archived, updated_at
  )
  values (
    v_draft,
    v_key,
    trim(p_label),
    coalesce(p_description, ''),
    case when trim(coalesce(p_category, '')) = '' then split_part(v_key, '.', 1) else lower(trim(p_category)) end,
    coalesce(p_is_founder_only, false),
    coalesce(p_is_archived, false),
    now()
  )
  on conflict (version_no, key) do update
  set
    label = excluded.label,
    description = excluded.description,
    category = excluded.category,
    is_founder_only = excluded.is_founder_only,
    is_archived = excluded.is_archived,
    updated_at = now();

  insert into public.platform_audit_events (
    actor_user_id, event_type, entity_type, entity_id, after_state, metadata
  )
  values (
    auth.uid(),
    'catalog.draft_entry_upserted',
    'permission_catalog_entry',
    v_key,
    jsonb_build_object(
      'version_no', v_draft,
      'key', v_key,
      'label', trim(p_label),
      'category', case when trim(coalesce(p_category, '')) = '' then split_part(v_key, '.', 1) else lower(trim(p_category)) end
    ),
    '{}'::jsonb
  );

  return v_draft;
end;
$$;

create or replace function public.platform_publish_catalog(
  p_publish_note text default ''
)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  v_draft bigint;
begin
  if not public.platform_is_founder(auth.uid()) then
    raise exception 'not allowed' using errcode = '42501';
  end if;
  select public.platform_ensure_latest_draft_catalog() into v_draft;

  update public.platform_permission_catalog_versions
  set status = 'published', published_by = auth.uid(), published_at = now(), publish_note = coalesce(p_publish_note, '')
  where version_no = v_draft;

  insert into public.permission_catalog (key, label, description, is_founder_only)
  select e.key, e.label, e.description, e.is_founder_only
  from public.platform_permission_catalog_entries e
  where e.version_no = v_draft
    and e.is_archived = false
  on conflict (key) do update
  set
    label = excluded.label,
    description = excluded.description,
    is_founder_only = excluded.is_founder_only;

  delete from public.permission_catalog pc
  where not exists (
    select 1
    from public.platform_permission_catalog_entries e
    where e.version_no = v_draft
      and e.key = pc.key
      and e.is_archived = false
  );

  insert into public.platform_audit_events (
    actor_user_id, event_type, entity_type, entity_id, after_state, metadata
  )
  values (
    auth.uid(),
    'catalog.published',
    'permission_catalog_version',
    v_draft::text,
    jsonb_build_object('version_no', v_draft, 'publish_note', coalesce(p_publish_note, '')),
    '{}'::jsonb
  );

  return v_draft;
end;
$$;

