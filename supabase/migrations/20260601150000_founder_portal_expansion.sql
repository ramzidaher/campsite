-- Founder portal expansion: versioned RBAC catalog, presets, audit, and governance controls.

create table if not exists public.platform_permission_catalog_versions (
  version_no bigint generated by default as identity primary key,
  status text not null default 'draft' check (status in ('draft', 'published')),
  created_by uuid references auth.users(id) on delete set null,
  published_by uuid references auth.users(id) on delete set null,
  publish_note text not null default '',
  created_at timestamptz not null default now(),
  published_at timestamptz
);

create table if not exists public.platform_permission_catalog_entries (
  version_no bigint not null references public.platform_permission_catalog_versions(version_no) on delete cascade,
  key text not null,
  label text not null,
  description text not null default '',
  category text not null default 'other',
  is_founder_only boolean not null default false,
  is_archived boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint platform_permission_catalog_entries_pkey primary key (version_no, key)
);

create table if not exists public.platform_role_presets (
  id uuid primary key default gen_random_uuid(),
  source_version_no bigint not null references public.platform_permission_catalog_versions(version_no) on delete cascade,
  key text not null,
  name text not null,
  description text not null default '',
  target_use_case text not null default '',
  recommended_permission_keys text[] not null default '{}',
  is_archived boolean not null default false,
  created_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (source_version_no, key)
);

create table if not exists public.platform_audit_events (
  id uuid primary key default gen_random_uuid(),
  actor_user_id uuid references auth.users(id) on delete set null,
  org_id uuid references public.organisations(id) on delete set null,
  event_type text not null,
  entity_type text not null,
  entity_id text not null,
  before_state jsonb not null default '{}'::jsonb,
  after_state jsonb not null default '{}'::jsonb,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create index if not exists platform_permission_catalog_entries_version_idx
  on public.platform_permission_catalog_entries(version_no, category, key);
create index if not exists platform_role_presets_version_idx
  on public.platform_role_presets(source_version_no, is_archived, name);
create index if not exists platform_audit_events_created_idx
  on public.platform_audit_events(created_at desc);
create index if not exists platform_audit_events_org_event_idx
  on public.platform_audit_events(org_id, event_type, created_at desc);

alter table public.org_roles
  add column if not exists source_preset_id uuid references public.platform_role_presets(id) on delete set null;
alter table public.org_roles
  add column if not exists source_catalog_version_no bigint;

alter table public.organisations
  add column if not exists plan_tier text not null default 'starter';
alter table public.organisations
  add column if not exists subscription_status text not null default 'active'
  check (subscription_status in ('active', 'limited', 'suspended'));
alter table public.organisations
  add column if not exists is_locked boolean not null default false;
alter table public.organisations
  add column if not exists maintenance_mode boolean not null default false;
alter table public.organisations
  add column if not exists force_logout_after timestamptz;

