create or replace function public.platform_ensure_latest_draft_catalog()
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  v_draft bigint;
  v_source bigint;
begin
  select public.platform_latest_draft_catalog_version() into v_draft;
  if v_draft is not null then
    return v_draft;
  end if;

  select public.platform_latest_published_catalog_version() into v_source;

  insert into public.platform_permission_catalog_versions (status, created_by)
  values ('draft', auth.uid())
  returning version_no into v_draft;

  if v_source is not null then
    insert into public.platform_permission_catalog_entries (
      version_no, key, label, description, category, is_founder_only, is_archived
    )
    select
      v_draft, e.key, e.label, e.description, e.category, e.is_founder_only, e.is_archived
    from public.platform_permission_catalog_entries e
    where e.version_no = v_source
    on conflict (version_no, key) do nothing;
  else
    insert into public.platform_permission_catalog_entries (
      version_no, key, label, description, category, is_founder_only, is_archived
    )
    select
      v_draft,
      pc.key,
      pc.label,
      pc.description,
      split_part(pc.key, '.', 1),
      pc.is_founder_only,
      false
    from public.permission_catalog pc
    on conflict (version_no, key) do nothing;
  end if;

  return v_draft;
end;
$$;

