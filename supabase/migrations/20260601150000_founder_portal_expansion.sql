create or replace function public.platform_update_org_governance(
  p_org_id uuid,
  p_plan_tier text,
  p_subscription_status text,
  p_is_locked boolean,
  p_maintenance_mode boolean,
  p_force_logout boolean default false
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_before jsonb;
begin
  if not public.platform_is_founder(auth.uid()) then
    raise exception 'not allowed' using errcode = '42501';
  end if;

  select jsonb_build_object(
    'plan_tier', o.plan_tier,
    'subscription_status', o.subscription_status,
    'is_locked', o.is_locked,
    'maintenance_mode', o.maintenance_mode,
    'force_logout_after', o.force_logout_after
  )
  into v_before
  from public.organisations o
  where o.id = p_org_id;

  update public.organisations
  set
    plan_tier = coalesce(nullif(trim(coalesce(p_plan_tier, '')), ''), plan_tier),
    subscription_status = case
      when p_subscription_status in ('active', 'limited', 'suspended') then p_subscription_status
      else subscription_status
    end,
    is_locked = coalesce(p_is_locked, is_locked),
    maintenance_mode = coalesce(p_maintenance_mode, maintenance_mode),
    force_logout_after = case when coalesce(p_force_logout, false) then now() else force_logout_after end
  where id = p_org_id;

  insert into public.platform_audit_events (
    actor_user_id, org_id, event_type, entity_type, entity_id, before_state, after_state, metadata
  )
  select
    auth.uid(),
    o.id,
    'org.governance_updated',
    'organisation',
    o.id::text,
    coalesce(v_before, '{}'::jsonb),
    jsonb_build_object(
      'plan_tier', o.plan_tier,
      'subscription_status', o.subscription_status,
      'is_locked', o.is_locked,
      'maintenance_mode', o.maintenance_mode,
      'force_logout_after', o.force_logout_after
    ),
    jsonb_build_object('force_logout_triggered', coalesce(p_force_logout, false))
  from public.organisations o
  where o.id = p_org_id;
end;
$$;

create or replace function public.platform_create_support_session(
  p_org_id uuid,
  p_target_user_id uuid,
  p_minutes integer default 15
)
returns text
language plpgsql
security definer
set search_path = public
as $$
declare
  v_token text;
  v_minutes integer := greatest(1, least(coalesce(p_minutes, 15), 120));
begin
  if not public.platform_is_founder(auth.uid()) then
    raise exception 'not allowed' using errcode = '42501';
  end if;

  v_token := encode(gen_random_bytes(24), 'hex');

