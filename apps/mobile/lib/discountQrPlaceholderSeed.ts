export function buildRandomSeed(rows: number, cols: number): number[][] {
  const out: number[][] = [];
  for (let ri = 0; ri < rows; ri++) {
    const row: number[] = [];
    for (let ci = 0; ci < cols; ci++) {
      row.push(Math.random() < 0.48 ? 1 : 0);
    }
    out.push(row);
  }
  return out;
}
